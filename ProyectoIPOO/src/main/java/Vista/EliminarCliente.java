    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        try{
            int cedula = Integer.parseInt(this.txfCedula.getText());
            String resultado = this.admAplicacion.getAdmCounter().eliminarCliente(cedula);
            if(resultado.equals("Cliente eliminado")){
              JOptionPane.showMessageDialog(null, resultado, "!", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(null, resultado, "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }catch(Exception e){
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null, "Algo sali√≥ mal, intenta ingresar los datos nuevamente de forma correcta", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

