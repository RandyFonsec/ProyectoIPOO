    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombre, direccion,correo;
        char sexo;
        int cedula, telefono, dia, anno, mes;
        try{
            nombre = this.txfNombre.getText();
            direccion = this.txfDireccion.getText();
            correo = this.txfCorreo.getText();
            sexo = (this.cbSexo.getSelectedItem().equals("Masculino") ? 'M':'F');
            telefono = Integer.parseInt(this.txfTelefono.getText());
            cedula = Integer.parseInt(this.txfCedula.getText());
            dia = Integer.parseInt((String)this.cbDia.getSelectedItem());
            anno = Integer.parseInt((String)this.cbAnno.getSelectedItem());
            mes = this.cbMes.getSelectedIndex();
            String resultado;
            resultado = this.admAplicacion.getAdmCounter().crearCliente(cedula, nombre, correo, telefono, direccion, sexo, dia, mes, anno);
            if(resultado.contains("Cliente creado")){
                JOptionPane.showMessageDialog(null, resultado, "!", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(null, resultado, "ERROR", JOptionPane.ERROR_MESSAGE);
            }
       }
       catch(Exception e){
           System.out.println(e.getMessage());
           JOptionPane.showMessageDialog(null, "Algo sali√≥ mal, intenta ingresar los datos nuevamente de forma correcta", "ERROR", JOptionPane.ERROR_MESSAGE);
       }
    }//GEN-LAST:event_jButton1ActionPerformed

